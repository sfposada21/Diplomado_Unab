    private void jBvalidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBvalidarActionPerformed
        
        String codigo = jTFuser.getText();
        miCliente = null;  
        
        for (ClientePremium miclientepre : misClientesPre) {                
            if ( (miclientepre.getCodigo()).equalsIgnoreCase(codigo)){
                miCliente = miclientepre;                                
                break;
            }  }
                           
            if (miCliente ==null){
               JOptionPane.showMessageDialog(this, "Codigo Invalido");  
               jTFnombre.setText("Invitado");
               jTFnombre.setEnabled(true);
            } else {
               JOptionPane.showMessageDialog(this, "Bienvenido :"+miCliente.getDescripcion());   
               jTFnombre.setText(miCliente.getNombre());
               jTFnombre.setEnabled(false);
               jScantidad.setValue(0);
            }
        
    }//GEN-LAST:event_jBvalidarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        int select = jTable.getSelectedRow();
        
        if (select != -1 ){            
            listaCompras.remove(select);
            cargarCompras();
            
        } else {
            JOptionPane.showMessageDialog(this,  "Debes selecionar una fila");   
        }        
    }//GEN-LAST:event_jButton3ActionPerformed

