    private void jBagregarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBagregarCompraActionPerformed
        
        Compra miCompra = new Compra();  
        
        int opc = jCBproductos.getSelectedIndex();
        int tipoLugar = jCBlugardeenvio.getSelectedIndex();        
        miCompra.tipoLugar = tipoLugar+1;
        miCompra.tipoEnvio = tipoEnvio;
        int bandera = 0;
        int cantidad = (int)jScantidad.getValue();
        miCompra.cantidad = cantidad;
        
        if (miCliente == null){
            miCliente = new Cliente();  
            miCliente.setNombre(jTFnombre.getText());
        }
        
        miCompra.cliente = miCliente;
        
        if (cantidad < 0){
            JOptionPane.showMessageDialog(this, "Fabricas de pendejos, se les escapo uno | cantidad debe ser posotivo");
        }        
        
        if(opc >0 && tipoEnvio > 0 && cantidad > 0){    
        miCompra.producto = listaProductos[opc-1];            
        bandera = 1;
        } else {
        JOptionPane.showMessageDialog(this, "debes rellenar todos los datos");       
        bandera=0;
        }
                            
        if (bandera==1){
        listaCompras.add(miCompra);
        cargarCompras();
        }
        
        
    }//GEN-LAST:event_jBagregarCompraActionPerformed

    private void cargarCompras(){
    
     String[] columnas = {"CLIENTE", "PRODUCTO","CANTIDAD", "ENVIO" ,"DESCUENTO" , "TOTAL"};
        miModelo = new DefaultTableModel(columnas, 0);        
        double total = 0;
        
        for (Compra laCompra : listaCompras) {
            miModelo.addRow(laCompra.getFila());
            total += laCompra.getTotal();
        }        
        
        jLtotal.setText("$"+String.format("%.1f", total));
        jTable.setModel(miModelo);    
        jCBproductos.setSelectedIndex(0);
    }
    
    
